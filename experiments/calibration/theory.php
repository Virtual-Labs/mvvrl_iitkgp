     <p class="style1">Inclined Tube Manometer:</p>
     <p>Inclined tube manometer is a simple and cheap instrument that is commonly&nbsp;   used for measuring differential pressure in a mine.&nbsp; It&nbsp; resembles to a   U-tube Manometer. One limb is a tube of uniform bore (6 mm internal   diameter) which is kept inclined at a low angle to the horizontal line.   The other limb is&nbsp; transformed to a large reservoir with cross-sectional   area 300-400 times larger than that of the cross-sectional area of  inclined limb. Any change in the level of manometer fluid in the   reservoir can be neglected in comparison with the change in fluid level   in the inclined tube.&nbsp; Hence, a scale along the inclined limb directly   reads pressure difference.</p>
        For greater sensitivity, the inclined tube manometers can be filled with   alcohol which also gives better meniscus than water. The   instrument can be made suitable to measure a wide range of pressures by   changing the inclination of tube. Smaller angle of inclination can be   used for measuring lower range of pressures.
