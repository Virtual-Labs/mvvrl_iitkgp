     <h3>&nbsp;Measurement of Pressure:</h3>
     <p>Though pressure is an absolute quantity, every measurement is made relative to reference like ambient air pressure.</p>
     <p><strong><em>1.&nbsp; Absolute Pressure:</em></strong> is referenced to zero(0) against perfect vacuum ( = gauge pressure + atmospheric pressure )</p>
     <p><strong><em>2.&nbsp; Gauge Pressure:</em></strong> is taken in reference with ambient air pressure.</p>
     <p><em><strong>3.&nbsp; Differential Pressure: </strong></em>is the difference in pressure between two points.</p>
