     <h3>Askania Minimeter:</h3>
     <p align="justify">This instrument consists of two inter-communicating containers filled   with distilled water. Container f is the observation vessel and N is the   balancing vessel. L is the nipple for connecting to the high pressure   side and D , to the low pressure side. Vessel N can be raised or lowered   by operating the knurled ring P, the amount of movement being noted on   the scale r graduated in millimetres and the micrometer scale S having a   least count of 0.01 mm.</p>
     <p align="justify"> At the outset the central point of   nipple D is brought to the zero of the scale R by turning of the   knurled ring P. Cock E is opened and vessel F is filled with water till   the pointer G just touches the water level. E is then closed. The pointer   is brought to touch the water level exactly by an adjustment of the   mild head H, the exact coincidence being observed through the lens K.</p>
     <p align="justify"> The two nipples are then connected   to the pressure lines when the level of water in F falls and that in N   rises. Now the vessel N is raised turning the knurled ring P until the   water level in F again touches the tip of the pointer&nbsp; G ( null point   ). The readings on the scale R and the micrometer S then give the   pressure in mm.</p>
