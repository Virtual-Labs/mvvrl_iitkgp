    <p>The objective of this experiment is to find out effieciency of an evasee in terms of pressure recovery in a mine ventilation system.<br />
      </p>
    <p><br />
      An evasee is a device which is usually attached at the outlet of the mine fan to recover some of the wasteful energy to the useful pressure energy. This recovered pressure energy in turn increases the air flow rate in the mine ventilation system. Basically an evasee is a gradually expanding conical frustum which converts a part of the kinetic energy to static energy by virtue of its area change (increase in area) between inlet to outlet. <br />
      The functional benefit of an evasee can be realized if the figure 1, which depicts performance characteristics curves of a mine ventilation systems with and without evasee, is consulted. In absence of evasee, the fan operates at the static pressure P1 and quantity Q1. When the evasee is added to the system, the static pressure increases to P2 and a result of which quantity increases to Q2. However, total pressure energy (static + kinetic) remains same for both of the cases. Therefore, with same power consumption extra quantity is added to the systems with the help of an evasee.<br />
      Ideally, full conversion of the expected kinetic pressure energy to static pressure energy is not achieved due to shock losses and friction losses (due to extra length of evasee in the system). The increase in area should be gradual and symmetrical to minimize the shock losses. In this respect, a suitable angle of divergence of the sides of the evasee is to be maintained. Smaller angle makes the evasee too long and expensive as well as long evasee increases frictional pressure losses. On the other hand, larger angle of divergence increases the shock loss in the evasee. Hence in order to realize optimum pressure head recovery of an evasee, it should be constructed suitably. In this experiment, the evasee with a divergence angle of 7&deg; and area ratio of 4:1 is used. The efficiency of this evasee is now needed to be evaluated through laboratory experiment. </p>
