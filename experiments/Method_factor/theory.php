     <p><br />
       <strong>Determination  of method factor of a duct&nbsp;</strong></p>
     <p><strong>Objective:</strong></p>
     <p>Objective of this experiment is to find out the  method factor of a duct so that from a single point measurement of velocity at  the centre, average velocity of flow over the duct cross section can be  obtained without traversing.</p>
     <p><strong>Theory:</strong></p>
     <p><strong>&nbsp;</strong>The air velocity varies from point to point over a  cross section of a duct. The velocity profile of air flow in a duct depends  upon Reynolds Number, surface roughness of the duct. Typically for laminar flow,  velocity profile resembles to parabolic shape and for turbulent flow, it takes  &lsquo;D&rsquo; shaped profile. Hence to estimate average flow velocity, traversing is  usually carried out at a cross section of the duct. There are two types of  traversing conducted to measure average velocity: continuous traversing and  precise traversing. However, traversing is more time consuming. In order to  obtain very rapid estimate of average air velocity, the point measurement of  velocity may be obtained, which then be multiplied by a method factor to obtain  average velocity. &nbsp;Hence, to do this the  method factor of the duct is to be known in priori. The value of method factor  decreases with decrease in Reynolds Number. For high Reynolds Number (in the  order of 50,000 and above) the method factor can be taken to be constant.</p>
     <p>To determine method factor of a duct, both central  velocity and average velocity may be taken, and then it can be calculated by  the following expression.</p>
