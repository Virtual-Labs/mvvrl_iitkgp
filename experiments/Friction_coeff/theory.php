     <h2 align="left">Determination of  frictional coefficient of a ventilation duct <u></u></h2>
     <h4 align="left">&nbsp;<strong>Objective</strong></h4>
     <p>To determine the frictional coefficient of duct for  mine airways<br />
         </p>
     <p><strong>Theory</strong> <br />
       Mine airways can be considered as very rough conduits  with incompressible flow so the frictional pressure drop is given as</p>
     <p align="center"><br />
  	&Delta;Pf  = f L &rho; v2 /  2 D&nbsp; </p>
     <p><br />
       Where,</p>
     <p align="center"><br />
       f = Resistance coefficient <br />
       L = Length of the duct &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (m)<br />
  &rho; = Air density &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (Kg/m3)<br />
       v = Air velocity &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (m/s)</p>
     <p align="left"><br />
       As D is taken as the equivalent diameter given by  the below relation </p>
     <p align="center"><br />
       D = 4A/P</p>
     <p align="left"><br />
       Where, </p>
     <p align="center"><br />
       A= Cross-sectional area of duct(m2)<br />
      P = Perimeter of the duct(m)</p>
     <p align="left"><br />
      So the above equation can be rewritten as:</p>
     <p align="center"><br />
  &Delta;Pf  = f L P &rho;  v2 / 8 A</p>
     <p align="left"><br />
      Flow of air in mine airways is rarely laminar (Re  &lt;= 2000) except for leakage through goaf or loose waste packs. So the above  equation becomes</p>
     <p align="center"> <br />
  &Delta;Pf&nbsp; =&nbsp; k L  P v2 / A<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; = k S  v2 / A<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; = k S Q2  / A3 &nbsp;&nbsp;(Pascal)</p>
     <p align="center"><br />
       Where, k = Coefficient of friction <br />
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp; = f &rho;  / 8  (N s2 m - 4)<br />
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; S =  Area of the rubbing surface = L P (m2)<br />
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Q =  Air flow rate = A v (M3/s)</p>
     <p align="left"><br />
       So, the graph of &Delta;Pf with respect to SQ2  /A3 will be plotted and the slope of the curve will be drowned. The  will be the coefficient of friction. </p>
