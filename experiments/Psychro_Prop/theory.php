     <p><strong>Dry Bulb Temperature (TD)</strong><br />
       The  dry-bulb temperature is the <a href="http://en.wikipedia.org/wiki/Temperature" title="Temperature">temperature</a> of <a href="http://en.wikipedia.org/wiki/Air" title="Air">air</a> measured by a <a href="http://en.wikipedia.org/wiki/Thermometer" title="Thermometer">thermometer</a> freely exposed to the air but shielded from <a href="http://en.wikipedia.org/wiki/Radiation" title="Radiation">radiation</a> and <a href="http://en.wikipedia.org/wiki/Moisture" title="Moisture">moisture</a>. Dry bulb temperature is the temperature that is  usually thought of as air temperature, and it is the true thermodynamic  temperature. It is the temperature measured by a regular thermometer exposed to  the airstream. The dry-bulb temperature is an indicator of heat content in the  air. <strong> </strong><br />
        </p>
     <p><strong>Wet Bulb  Temperature (</strong><strong>TW</strong><strong>)</strong><br />
        <strong>&nbsp;</strong>This is the temperature recorded by a thermometer  whose bulb is covered with a thin cotton gauge which is dipped at the lower end  into water so that the surface of the bulb is kept moist by capillary action.<strong> </strong>If such a  thermometer is placed in unsaturated air, the wet bulb of the thermometer will  lose latent heat in the process of evaporation of moisture surrounded with the  bulb. &nbsp;As a result, the temperature of  the bulb will fall. &nbsp;Once the air is  saturated, there will be no further evaporation and cooling of the thermometer, then  it will show a constant temperature. This temperature is called Wet bulb  temperature (TW). Thus, the wet bulb temperature (TW), in  conjunction with the dry bulb temperature (TD), gives a measure of  the degree of saturation or the Relative Humidity (RH) of air <br />
        </p>
