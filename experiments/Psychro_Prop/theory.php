      <p><strong><em>Fortin Barometer</em></strong> <br />
      It  is an instrument which works on mercury level measurement. It has a long glass tube,  filled with mercury. &nbsp;The height of  mercury column which indicates the pressure can be read from the scale on brass  tube. As atmospheric pressure changes, the mercury level in the cistern changes  accordingly. For example, an increase in atmospheric pressure causes the  mercury level to rise. The mercury is contained within a flexible pouch and  screw jack causing the level either to rise or fall. By moving the screw of  vernier, the top of the column is matched with the lower edge of the vernier  and the reading is then taken from main scale and vernier scale. The mercury is  toxic; hence, direct contact should be avoided while using the instrument. When  adjusting the mercury level to point zero, the adjusting screw should not be  turned too quickly and if the mercury has been moved through a large amount,  the instrument should be left to settle before making the final setting.</p>
