     <p><strong><em>Aneroid Barometer</em></strong> <br />
       An  aneroid barometer uses a small, flexible metal box called an aneroid cell. This  cell is made from an alloy of beryllium and copper. The evacuated capsule is  presented from collapsing by a string spring. Small changes in the outside air cause  the box to expand or contract. Small levers, present in the barometer amplify  the small changes and cause the needle to show the pressure. The mechanism is  made deliberately &quot;stiff&quot; so that, tapping the barometer reveals  whether the barometer is rising or falling as the pointer moves.<br />
     </p>
