     <p><strong><em>Assmann Psychrometer</em></strong><br />
       Assmann  psychrometer is similar to whirling hygrometer by operational principle.  However, two thermometers in this psychrometer are ventilated by a fan operated  by a clock-type spring. The fan should be run for 3 minutes before taking a  reading. Chromium-plated metal sleeves of the bulbs prevent radiation from  surroundings. The least count of the Assmann psychrometer is 0.2&deg;C. So, this  instrument is five times more accurate than whirling hygrometer.<br />
     </p>
