    <p><strong>Familiarization  with Assmann psychrometer and estimation of psychrometric properties of air. </strong><br />
       </p>
    <p><strong>&nbsp;<u>Objective: </u></strong><br />
      Estimation  of psychrometric properties of air relevant to climatic conditions of an  environment and acquire skill for handling Assmann psychrometer.<br />
  </p>
    <p><strong><u>Theory:</u></strong><br />
      Temperature  and humidity of air determine the climatic condition of work environment. As  the climatic condition becomes more hot and humid, the work efficiency of the workers  reduces. More over workers may feel tired which may lead to accident  occasionally, and in extreme cases fatality may occur due to heat stress. Hence  determining the psychrometric properties of air which are related to heat and  humidity condition is very important to assess the quality of work space  environment. The following psychrometric properties are relevant for  determining climatic conditions.</p>
