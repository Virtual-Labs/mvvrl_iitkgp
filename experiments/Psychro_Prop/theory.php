     <p><strong><u>&nbsp;</u></strong></p>
     <p>Various  instruments are used to measure the dry-bulb and wet-bulb temperature of  air.&nbsp; Among them, Whirling hygrometer and  Assmann Psychrometer are most common.&nbsp;  The atmospheric pressure can be measured with the help of Fortin barometer  or Aneroid barometer. Once, the dry bulb temperature, wet-bulb temperature and  barometric pressure are measured, all the psychrometric properties are  determined using those measured values.&nbsp;&nbsp;  A brief overview of these instruments is provided below.</p>
     <p><strong><em>Whirling  Hygrometer or Sling Psychrometer</em></strong> <br />
       This  instrument consists of a wet-bulb thermometer and a dry-bulb thermometer.&nbsp; These two thermometers are fastened on cork  mounted in a wooden frame with arrangement for rotating the same to provide a  relative movement of air around the thermometer bulbs. The two bulbs are placed  in such a way that the evaporative cooling and moisture from wet-bulb do not  affect the dry bulb. It is provided with a handle for whirling the instrument  such that continuous rotation of the bulb causes the fresh air to pick up  moisture surrounding the wet-bulb. The instrument is rotated at the rate of  20rad/s so that relative air velocity of 3m/s can be maintained. The least  count of this instrument is 1&deg;C.<br />
     </p>
