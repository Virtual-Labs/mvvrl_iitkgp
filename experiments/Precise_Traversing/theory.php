      <strong>Precise traversing in a  ventilation duct using Pitot tube</strong> <br />
</p>
    <p><strong>Objective:</strong> </p>
    <p>The purpose of this experiment is to finding the velocity profile of air flow  in a ventilation duct and estimation of average velocity of air from the  different point measurements in a cross-section. <br />
        </p>
    <p><strong>Theory:</strong><br />
      </p>
    <p>The  velocity of air flow varies from point to point over the cross-section of a  duct airway and the variation is irregular in nature, particularly if the duct airway  has rough sides and is not straight. The velocity profile of airway can change  depending upon the Reynolds number of the flow, surface roughness, and the  nature of the flow e.g. whether the flow is under developed or fully developed.  The typical velocity profile of a circular duct for a fully developed flow is  in parabolic shape for laminar flow and D-shaped for a turbulent flow.&nbsp; The nature of the velocity profile has been  discussed in number text books (Misra, 1988; Harman and Mutmansky, 1997; McPherson,  1993).&nbsp; The nature of the velocity  profile can be observed through a laboratory experiment. <br />
      Besides  the study of velocity profile, the average velocity can also be estimated by  measuring the air velocity at the different points of the cross-section. In  general, average air velocity can be measured by two methods: (i) continuous  traversing, and (ii) precise traversing.&nbsp;  The continuous traversing, although simple and rapid; it is not highly  accurate (accuracy is almost 5%). Precise traversing is time consuming but very  accurate. An accuracy of 2% can be achieved by this method. In order to conduct  the precise traversing, the whole cross-section is divided into certain number  of equal areas. The number of division depends upon the number of measuring  points. The instrument measures the velocity at the midpoint of these equal  areas. For a typical circular duct, these equal areas essentially become  concentric circles of different radius. The locations of the measuring points  can be derived using the following formula and shown in Figure1.</p>
